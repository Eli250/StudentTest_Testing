    private void comboFaculty() {
        List<Faculty> fl = facultyDao.findAll();
        facCombo.removeAllItems();
        for (Faculty faculty : fl) {
            facCombo.addItem(faculty.getId() + " - " + faculty.getName());
        }
    }

    private void comboStudent() {
        List<Department> dl = departmentDao.findAll();
        studentCombo.removeAllItems();
        for (Department d : dl) {
            studentCombo.addItem(d.getDepId());
        }
    }

    private void comboCourse() {
        List<Department> dList = departmentDao.findAll();
        courseCombo.removeAllItems();
        for (Department d : dList) {
            courseCombo.addItem(d.getDepId());
        }
    }

