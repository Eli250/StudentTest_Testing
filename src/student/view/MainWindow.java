    private void studentSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentSaveActionPerformed
        Student st = new Student();
        st.setStudentId(studIdTxt.getText());
        st.setName(fullNameTxt.getText());
        st.setPhoneNumber(phonenumberTxt.getText());
        st.setEmailAddress(emailTxt.getText());
        Date dob = dobTxt.getDate();
        st.setDateOfBirth(getLocalDate(dob));
        st.setGender(maleRdBtn.isSelected() ? Gender.MALE : Gender.FEMALE);
        Department dep = new Department();
        dep.setDepId(studentCombo.getSelectedItem().toString());
        st.setDepartment(dep);
        studentDao.create(st);
        populateStudent();
        JOptionPane.showMessageDialog(rootPane, "Student Added!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_studentSaveActionPerformed
    public LocalDate getLocalDate(Date date) {
        return date.toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
    }

    private void populateStudent() {
        List<Student> sList = studentDao.findAll();
        DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
        model.setRowCount(0);
        for (Student s : sList) {
            Object[] obj = {s.getStudentId(), s.getName(), s.getGender(),
                s.getDateOfBirth().toString(), s.getEmailAddress(), s.getPhoneNumber(), s.getDepartment().getDepId()};
            model.addRow(obj);
        }
    }

